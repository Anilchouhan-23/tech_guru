export function getSoftwareBySlug(slug) {
  return softwareList.find((s) => s.slug === slug);
}

export function getSoftwareByCategory(category) {
  return softwareList.filter((s) => s.category === category);
}

export function getFeaturedSoftware() {
  return softwareList.filter((s) => s.featured);
}

export function searchSoftware(query) {
  const q = query.toLowerCase();
  return softwareList.filter(
    (s) =>
      s.name.toLowerCase().includes(q) ||
      s.shortDescription.toLowerCase().includes(q) ||
      s.category.toLowerCase().includes(q)
  );
}
